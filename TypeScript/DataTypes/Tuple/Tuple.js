"use strict";
// Тип ⁡⁢⁣⁣Tuple⁡ или кортеж представляет собой ⁡⁣⁣⁢массив⁡, каждому из элементов которого ⁡⁣⁣⁢можно указать свой тип⁡. 
// Обычно такая структура используется для предоставления набора данных, например, записи в базе данных.
var isTuple = ['string1', false];
console.log(isTuple[0] + ', Тип: ' + typeof isTuple[0]); // Выведет: Тип string
console.log(isTuple[1] + ', Тип: ' + typeof isTuple[1]); // Выведет: Тип number
